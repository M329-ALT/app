package com.example.restaurants_3;

public class Meal {
    private int id;
    private String name;
    private String description;
    private double price;
    private int imageUrl;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public double getPrice() {
        return price;
    }

    public int getImageUrl() {

        return  imageUrl;
    }
}
